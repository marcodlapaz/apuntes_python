# Potenciación (**)
exponente = 2 ** 10
print(exponente)        # 1024

# División baja (//)
division_baja = 12 // 4     
print(division_baja)    # 3 Hace el redondeo hacia abajo

# Resto o módulo
resto = 12 % 5
print(resto)            # 2

# Ver el tipo de dato
print(type(10))         # <class 'int'>
print(type("hola"))     # <class 'str'>
