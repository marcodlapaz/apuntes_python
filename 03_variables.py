# =========== Asignación de valores ===========
nombre = "Marco"
nombre = "Pedro"
nombre = "Alex"

print(nombre)

# =========== Acumulación con las variables ===========
numero = 10
numero += 1
print(numero)

# =========== Concatenar con f-strings ===========
usuario = "Windows"
mensaje = f"Hola {usuario} ¿cómo estas?"
print(mensaje)

# =========== Concatenar con + ===========
mensaje = "Hola " + "¿cómo estas?"
print(mensaje)

# =========== Operadores in / not in ===========
texto = "Hola mundo"
print("ola" in texto)       # True
print("hola" in texto)      # False (Python es case sensitive)
print("Hola" not in texto)  # False porque negamos la salida original de True

